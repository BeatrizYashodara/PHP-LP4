3-OperadoresCombinados.php
<?php

/*
Operador	Equivale a
+=	        x = x + valor
-=	        x = x - valor
*=	        x = x * valor
/=	        x = x / valor
%=	        x = x % valor
*/


$x = 10;

$x += 5; // x = 10 + 5
echo $x; // 15

$x -= 3; // x = 15 - 3
echo $x; // 12

$x *= 2; // x = 12 * 2
echo $x; // 24

$x /= 4; // x = 24 / 4
echo $x; // 6


?>
