<?php

// Operadores de Incremento e Decremento

/* Permitem adicionar ou subtrair uma unidade da variável.
*/

$y = 1;
echo $y = 1;
echo "<br>";

#Tradicional
$y = 1+1;
echo $y = 1;
echo "<br>";

# Pré-Incremento
++$y;
echo $y;
echo "<br><br>";

# Pós-Incremento
$y++;
echo $y;
echo "<br><br>";

# Pós-Decremento
$y--;
echo $y;
echo "<br><br>";

# Pré-Decremento
--$y;
echo $y;
echo "<br><br>";


?>
