<?php

/*

1 - OPERADORES ARITMÉTICOS

Operador	Significado

+                | soma |
-                | subtração |
*                | multiplicação |
/                | divisão |
%                | resto (módulo) |
**               | potência |

*/

$a = 10;
$b = 3;

// Soma
echo $a + $b; // 13

// Subtração
echo $a - $b; // 7

// Multiplicação
echo $a * $b; // 30

// Divisão
echo $a / $b; // 3.3333

// Resto da divisão
echo $a % $b; // 1

// Potência
echo $a ** $b; // 1000 (10^3)
?>
