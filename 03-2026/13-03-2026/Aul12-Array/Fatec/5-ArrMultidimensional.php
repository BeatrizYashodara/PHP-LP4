<?php

// Array Multidimensional

/**
 * São arrays numéricos ou
 *  associativos, cujos elementos são outros arrays.
 * 
 * É uma estrutura multidimensional, pois possui
 *  elementos de array dentro de um outro array.
 */

# exemplos

# 1 - multidimensional numérico
$cod_lanches_precos = [
    [0101, 18.50],
    [0102, 21.5],
    [0103, 25.6]
];

# 2 - multidimensional associativo
$fileira_nomes = [
    ['3' => 'expelliarmus'],
    ['5' => 'oculus reparo'],
    ['2' => 'alohomora']
];

# 3 - multidimensional misto

$alturas_alunos = [
    ['otávio', 1.69],
    ['wallyson', 1.70], 
    ['gaby', 1.58],
    ['max', 1.85],
    ['beia', 1.58]
];


?>
